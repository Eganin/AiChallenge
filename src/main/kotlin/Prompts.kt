package org.example

object Prompts {

    const val WAIFU_QUESTION = "Как найти вайфу"

    fun temperatureCompare(r0: String, r07: String, r12: String) = """
        Тебе даны три ответа на вопрос: "$WAIFU_QUESTION"
        Ответы получены с разными значениями параметра temperature.

        ОТВЕТ #1 (temperature = 0.0 — детерминированный, нулевая случайность):
        $r0

        ОТВЕТ #2 (temperature = 0.7 — балансный, умеренная креативность):
        $r07

        ОТВЕТ #3 (temperature = 1.2 — значение за пределами допустимого диапазона [0.0; 1.0]):
        $r12

        Проведи сравнительный анализ по четырём критериям:

        1. Сравнение точности: насколько каждый ответ фактически верен, логичен и конкретен.
           Если ответ #3 содержит ошибку API — объясни, что означает это ограничение.

        2. Сравнение креативности: оригинальность формулировок, нестандартные идеи,
           образность и живость языка в каждом ответе.

        3. Сравнение разнообразия: количество уникальных советов, широта охвата темы,
           вариативность подходов и идей.

        4. Для каких задач лучше подходит каждая настройка:
           - temperature = 0.0: когда использовать, примеры задач
           - temperature = 0.7: когда использовать, примеры задач
           - temperature = 1.2: почему это значение недопустимо и что это означает на практике

        Структурируй ответ строго по этим четырём пунктам с заголовками.
    """.trimIndent()
}
